// SPIx 读写一个字节
// TxData:要写入的字节
// 返回值:读取到的字节
uint8_t SPI1_ReadWriteByte(uint8_t TxData)
{
	uint8_t retry = 0;
	while (SPI_SR(SPI1) != SPI_SR_TXE) // 检查指定的SPI标志位设置与否:发送缓存空标志位
	{
		retry++;
		if (retry > 200)
			return 0;
	}
	spi_send(SPI1, TxData); // 通过外设SPIx发送一个数据
	retry = 0;

	while (SPI_SR(SPI1) != SPI_SR_RXNE) // 检查指定的SPI标志位设置与否:接受缓存非空标志位
	{
		retry++;
		if (retry > 200)
			return 0;
	}
	return spi_read(SPI1); // 返回通过SPIx最近接收的数据
}
